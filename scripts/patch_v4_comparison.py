    "        fd=np.zeros(N); fd[sm_]=-ss_[sm_]; fd[bm]=bs_[bm]\n"
    "        cash=cas-bn-bf; shares=np.maximum(shares+fd,0.0)\n"
    "        nav_post[t]=cash+np.dot(shares,p); fee_a[t]=sf+bf\n"
    "        sc_a[t]=np.sum(bs_*(bpx-p))+np.sum(ss_*(p-spx))\n"
    "        to_a[t]=(sn+bn)/nb_ if nb_>1e-12 else 0\n"
