    "    if T>=2: rg[:-1]=nav_pre[1:]/nav_post[:-1]-1; rn[:-1]=nav_post[1:]/nav_post[:-1]-1\n"
    "    return pd.DataFrame({'TradingDay':days,'nav_pre':nav_pre,'nav_post':nav_post,\n"
